package array;

public class Loop {

	public static void main(String[] args) {
		
		// while文サンプルコード
		// int iを初期化
		int i = 0;
		// iの値を３回繰り返す処理
		while ( i < 3 ) {
			System.out.println("i = " + i);
			// 変数のカウントアップ
			i++;
		}
		
		// for文サンプルコード
		/*
		for ( int  = 0; i < 3; i++ ) {
			System.out.println("i = " + i);
		}
		*/
		
	}

}
